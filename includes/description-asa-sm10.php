                                <h3>Servicio y soporte:</h3>
                                <p align="justify">Cisco adopta un enfoque de ciclo de vida para los servicios y, junto con sus socios, proporciona una amplia cartera de servicios de 
                                    seguridad para que las empresas puedan diseñar, implementar, operar y optimizar plataformas de red que defienden los procesos comerciales críticos 
                                    contra ataques e interrupciones, protegen la privacidad y respaldan políticas y normativas. controles de cumplimiento.</p>
                                <p align="justify">Los servicios de Cisco lo ayudan a proteger su inversión en red, optimizar las operaciones de red y preparar su red para nuevas 
                                    aplicaciones a fin de ampliar la inteligencia de la red y el poder de su empresa.</p>
                                <p align="justify">Los siguientes servicios de seguridad de Cisco admiten y complementan los módulos SSM-AIP y los dispositivos de seguridad adaptable 
                                    de la serie ASA 5500:</p>
                                
                                <h4>Servicios de Cisco para IPS</h4>
                                <p align="justify">Los servicios de Cisco para IPS ayudan a proteger su empresa contra vulnerabilidades de seguridad al brindar soporte de hardware y 
                                    software, actualizaciones de aplicaciones y sistemas operativos, acceso a especialistas en ingeniería de seguridad de Cisco y alertas oportunas sobre 
                                    virus, gusanos y otras amenazas de última hora.</p>
                                <p align="justify">Servicios de Cisco para funciones de IPS:</p>
                                <ul>
                                    <li>Actualizaciones y alertas de archivos de firmas</li>
                                    <li>Acceso registrado a Cisco.com para herramientas en línea y asistencia técnica</li>
                                    <li>Acceso al Centro de asistencia técnica de Cisco (TAC)</li>
                                    <li>Actualizaciones del software del sensor IPS de Cisco</li>
                                    <li>Opciones para el reemplazo avanzado de hardware defectuoso</li>
                                </ul>
                                <h4>Centro de seguridad de Cisco</h4>
                                <p align="justify">El Centro de seguridad de Cisco ofrece una ventanilla única para el análisis de vulnerabilidades y amenazas de inteligencia de 
                                    amenazas de alerta temprana, firmas IPS de Cisco y técnicas de mitigación.</p>

                                <h4>Administrador de alertas de Cisco Security Intellishield</h4>
                                <p align="justify">El servicio Cisco Security Intellishield Alert Manager proporciona un servicio de alerta de amenazas y vulnerabilidades personalizable 
                                    basado en la web que permite a las organizaciones acceder fácilmente a información oportuna, precisa y creíble sobre posibles vulnerabilidades en su entorno.</p>

                                <h4>Servicio de optimización de seguridad de Cisco</h4>
                                <p align="justify">Servicio de optimización de seguridad de Cisco: cada vez más, la infraestructura de red es la base de un negocio ágil y adaptable. El 
                                    servicio de optimización de seguridad de Cisco es compatible con el sistema de seguridad en constante evolución para hacer frente a las amenazas de 
                                    seguridad en constante cambio, a través de una combinación de planificación y evaluaciones, diseño, ajuste del rendimiento y soporte continuo para 
                                    los cambios del sistema y ayuda a integrar la seguridad en la infraestructura de red central.</p>
                                
