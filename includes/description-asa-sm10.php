                                <h3>Descripción</h3>
                                <p align="justify">El módulo de servicios de seguridad de prevención e inspección avanzada de Cisco (AIP-SSM) para el dispositivo de seguridad 
                                    adaptable Cisco ASA serie 5500 proporciona servicios de prevención de intrusiones proactivos y con todas las funciones para detener el 
                                    tráfico malicioso, incluidos gusanos y virus de red, antes de que puedan afectar su red.</p>

                                <h4>Módulo de servicios de seguridad</h4>

                                <p align="justify">Al brindar una protección incomparable para los activos de información críticos de una organización, el dispositivo de seguridad
                                    adaptable Cisco ASA serie 5500 brinda las mejores capacidades de firewall y VPN en su clase en una única plataforma fácil de implementar. Cuando
                                    se combina con las capacidades de inspección avanzadas del AIP-SSM, el dispositivo de seguridad adaptable Cisco ASA serie 5500 brinda protección 
                                    integrada y convergente de los servidores y la infraestructura de una organización sin comprometer la capacidad de usar la red como una herramienta 
                                    comercial.</p>


                                <h4>Servicios de prevención de intrusiones AIP-SSM</h4>
                                <p align="justify">Cisco AIP-SSM combina servicios de prevención en línea con tecnologías innovadoras para mejorar la precisión. El resultado es una 
                                    confianza total en la protección que ofrece su solución de sistema de prevención de intrusiones (IPS), sin temor a que se pierda el tráfico legítimo. 
                                    Cuando se implementa dentro de los dispositivos Cisco ASA de la serie 5500, el AIP-SSM ofrece una protección integral de su red al colaborar con otros
                                    recursos de seguridad de la red, proporcionando un enfoque proactivo para proteger su red.
                                    <p>Cisco AIP SSM ayuda a los usuarios a detener las amenazas con mayor confianza mediante el uso de:</p>

                                    <ul>
                                        <li><p align="justify"><strong>Tecnologías de prevención en línea precisas</strong> - Proporciona una capacidad incomparable para tomar medidas preventivas 
                                        contra una gama más amplia de amenazas sin el riesgo de reducir el tráfico legítimo. Estas tecnologías únicas ofrecen un análisis contextual inteligente, 
                                        automatizado de sus datos y ayudan a garantizar que esté aprovechando al máximo su solución de prevención de intrusiones.</p></li>
                                        <li><p align="justify"><strong>Identificación de amenazas multivectorial</strong> - Protege su red de violaciones de políticas, explotaciones de vulnerabilidades 
                                        y actividad anómala mediante una inspección detallada del tráfico en las Capas 2 a 7.</p></li>
                                        <li><p align="justify"><strong>Colaboración de red única</strong> - Mejora la escalabilidad y la resistencia a través de la colaboración en red, incluidas 
                                        técnicas eficientes de captura de tráfico, capacidades de equilibrio de carga y visibilidad del tráfico cifrado.</p></li>
                                        <li><p align="justify"><strong>Potentes servicios de gestión, correlación de eventos y soporte</strong> - Permite una solución completa, que incluye 
                                        configuración, administración, correlación de datos y servicios de soporte avanzados. En particular, el Sistema de respuesta, análisis y monitoreo de 
                                        seguridad de Cisco (Cisco Security MARS) identifica, aísla y recomienda la eliminación precisa de los elementos ofensivos, para una solución de prevención 
                                        de intrusiones en toda la red. Y el Sistema de control de incidentes de Cisco (ICS) previene nuevos brotes de gusanos y virus al permitir que la red se adapte 
                                        rápidamente y proporcione una respuesta distribuida.</p></li>
                                    </ul>
                                    
                                </p>
                                <p align="justify">Cuando se combinan, estos elementos brindan una solución integral de prevención en línea, lo que le brinda la confianza para detectar y detener la gama más amplia 
                                    de tráfico malicioso antes de que la continuidad de su negocio se vea afectada.</p>

