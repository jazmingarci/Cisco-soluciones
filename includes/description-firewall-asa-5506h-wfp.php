                                <h3>Descripción general:</h3>
                                <p align="justify">Conozca el primer firewall de próxima generación (NGFW) adaptable y centrado en amenazas 
                                    de la industria, diseñado para una nueva era de protección avanzada contra amenazas y malware. Cisco ASA 
                                    con FirePOWER Services ofrece una defensa contra amenazas integrada para todo el continuo del ataque, 
                                    antes, durante y después de un ataque, al combinar las capacidades de seguridad comprobadas del firewall 
                                    Cisco ASA con las funciones de protección contra amenazas y malware avanzadas de Sourcefire, líderes en la 
                                    industria, juntas en una dispositivo único. La solución amplía de manera única, las capacidades de los firewalls 
                                    de próxima generación Cisco ASA serie 5500-X más allá de lo que las soluciones NGFW actuales son capaces de hacer.<br>
