                                <h2>Descripción general:</h2>
                                <p align="justify">La mayoría de los firewalls de próxima generación (NGFW) se centran en gran medida en habilitar
                                    el control de aplicaciones, pero poco en sus capacidades de defensa contra amenazas. Para compensar, algunos
                                    NGFW intentarán complementar su prevención de intrusiones de primera generación con una serie de productos
                                    complementarios no integrados. Sin embargo, este enfoque hace poco para proteger su empresa contra los riesgos
                                    que plantean los atacantes sofisticados y el malware avanzado. Además, una vez que se infecta, no ofrecen ayuda 
                                    para determinar el alcance de la infección, contenerla y remediarla rápidamente.</p>

                                <p align="justify">Lo que necesita es un firewall de próxima generación integrado y centrado en amenazas. Uno que 
                                    no solo brinda control granular de aplicaciones, sino que también brinda seguridad efectiva contra las amenazas 
                                    planteadas por ataques de malware sofisticados y evasivos.</p>

                                <p align="justify">Cisco Firepower Next-Generation Firewall (NGFW) es el primer NGFW totalmente integrado y centrado 
                                    en amenazas de la industria. Ofrece una gestión integral y unificada de políticas de funciones de firewall, control 
                                    de aplicaciones, prevención de amenazas y protección avanzada contra malware desde la red hasta el punto final.</p>

                                <p align="justify">Se puede implementar en dispositivos Cisco Firepower 2100 Series, 4100 Series y 9300 para proporcionar 
                                    una plataforma de seguridad NGFW optimizada para el rendimiento y la densidad para el perímetro de Internet y otros 
                                    entornos de alto rendimiento.</p>

