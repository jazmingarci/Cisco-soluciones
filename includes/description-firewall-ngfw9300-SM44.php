                                <h4>Tecnologías de anclaje de confianza de Cisco</h4>
                                <p align="justify">Las tecnologías de anclaje de confianza de Cisco proporcionan una base muy segura para ciertos productos de Cisco.
                                    Permiten garantizar la autenticidad del hardware y software para la confianza de la cadena de suministro y una fuerte mitigación 
                                    contra un compromiso de intermediario de software y firmware.</p>
                                <p>Las capacidades de Trust Anchor incluyen:</p>
                                <ul>
                                    <li><strong>Firma de imagen:</strong> Las imágenes firmadas criptográficamente brindan la seguridad de que el firmware, BIOS y otro 
                                    software son auténticos y no están modificados. A medida que se inicia el sistema, se comprueba la integridad de las firmas del software 
                                    del sistema.</li>
                                    <li><strong>Arranque seguro:</strong>El arranque seguro ancla la cadena de confianza de la secuencia de arranque al hardware inmutable,
                                    mitigando las amenazas contra el estado fundamental de un sistema y el software que se va a cargar, independientemente del nivel de 
                                    privilegios del usuario. Proporciona protección en capas contra la persistencia de firmware modificado ilícitamente.</li>
                                    <li><strong>Módulo Trust Anchor:</strong>Una solución de un solo chip a prueba de manipulaciones, criptográfica fuerte proporciona 
                                    garantía de autenticidad del hardware para identificar de forma única el producto, de modo que su origen pueda ser confirmado a Cisco,
                                     lo que garantiza que el producto es genuino.</li>
                                </ul>
