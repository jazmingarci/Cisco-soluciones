                                <h3>Despliegue</h3>

                                    <p align="justify">TCisco WSA es un proxy de reenvío que se puede implementar en modo explícito (archivos de configuración automática de proxy [PAC], descubrimiento automático de proxy web [WPAD], 
                                        configuración del navegador) o en modo transparente (protocolo de comunicación de caché web [WCCP], política- Enrutamiento basado en [PBR], balanceadores de carga). Los dispositivos compatibles con WCCP, 
                                        como los switches Cisco Catalyst® 6000 Series, los routers de servicios de agregación Cisco ASR 1000 Series, los routers Cisco Integrated Services Routers y los firewalls Cisco ASA 5500-X Series Next-Generation, 
                                        redireccionan el tráfico web a Cisco WSA.</p>

                                    <p align="justify">Cisco WSA puede utilizar como proxy HTTP, HTTPS, SOCKS, FTP nativo y FTP sobre tráfico HTTP para ofrecer capacidades adicionales como prevención de pérdida de datos, seguridad de usuarios móviles y 
                                        visibilidad y control avanzados.</p>
                               
                                
