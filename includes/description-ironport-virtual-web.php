                                <p align="justify">Por seguridad, su red necesita protección contra malware, visibilidad y control de aplicaciones, controles de políticas de uso aceptable, informes detallados y movilidad segura. Cisco ofrece 
                                    esta protección, todo en una sola plataforma: Cisco® Web Security Appliance (WSA).
                                </p>

                                <p align="justify">En nuestro mundo altamente conectado y cada vez más móvil, las amenazas más complejas y sofisticadas requieren la combinación adecuada de soluciones de seguridad. Cisco ofrece seguridad para 
                                    todas las capas de la infraestructura de red con la protección sólida, el control completo y el valor de inversión que necesitan las empresas. También ofrecemos un amplio conjunto de opciones de implementación 
                                    de seguridad web, junto con inteligencia de amenazas global líder en el mercado. Cisco WSA simplifica la seguridad con un dispositivo dedicado de alto rendimiento, y el dispositivo virtual de seguridad web de 
                                    Cisco (WSAV) permite a las empresas implementar seguridad web rápida y fácilmente, donde y cuando sea necesario.</p>

                                <p align="justify">Cisco WSA fue una de las primeras puertas de enlace web seguras que combinó protecciones líderes para ayudar a las organizaciones a abordar los crecientes desafíos de proteger y controlar el 
                                    tráfico web. Permite una implementación más simple y rápida con menos requisitos de mantenimiento, latencia reducida y costos operativos más bajos. La tecnología "Configurar y olvidar" libera al personal 
                                    después de que se activa la configuración de políticas automatizada inicial, y las actualizaciones de seguridad automáticas se envían a los dispositivos de red cada 3 a 5 minutos. Las opciones de implementación 
                                    flexibles y la integración con su infraestructura de seguridad existente lo ayudan a cumplir con los requisitos de seguridad en rápida evolución.</p>

                                <h4>Dispositivo virtual</h4>

                                <p align="justify">Con el crecimiento del video y otros medios enriquecidos, el tráfico se ha vuelto menos predecible, lo que resulta en excedentes y un rendimiento degradado. Al abordar estos y otros problemas, 
                                    los administradores enfrentan largos plazos de entrega al comprar e instalar hardware, desafíos de instalación remota, aranceles aduaneros y otros problemas logísticos, especialmente en organizaciones multinacionales.</p>

                                <p align="justify">Cisco WSAV reduce significativamente el costo de implementación de seguridad web, especialmente en redes altamente distribuidas, al permitir que los administradores creen instancias de seguridad 
                                    donde y cuando se necesiten. Cisco WSAV es una versión de software de Cisco WSA que se ejecuta sobre un hipervisor VMware ESXi o KVM y servidores Cisco Unified Computing System ™ (Cisco UCS®). Recibirá una 
                                    licencia ilimitada para Cisco SMAV con la compra de cualquiera de los paquetes de software Cisco Email o Web Security, junto con la licencia de software SMA correspondiente.</p>

                                <p align="justify">Con Cisco WSAV, los administradores pueden responder instantáneamente a los picos de tráfico y eliminar la planificación de la capacidad. No es necesario comprar ni enviar electrodomésticos; 
                                    Se pueden respaldar nuevas oportunidades comerciales sin agregar complejidad a un centro de datos o sin requerir personal adicional.</p>
                               
