                                <h3>Características y Beneficios:</h3>
                                
                                <h4>Inteligencia de seguridad de Talos</h4>
                                <p align="justify">Reciba una protección web rápida y completa respaldada por la red de detección de amenazas más grande del mundo, con la visibilidad más amplia y la huella más grande, que incluye:</p>
                               
                                    <ul>
                                        <li>100 TB de inteligencia de seguridad al día</li>
                                        <li>1,6 millones de dispositivos de seguridad implementados, incluidos firewall, IPS, web y dispositivos de correo electrónico</li>
                                        <li>150 millones de terminales</li>
                                        <li>13 mil millones de solicitudes web por día</li>
                                        <li>35% del tráfico de correo electrónico empresarial del mundo</li>
                                    </ul>
                                        
                                <p align="justify">Proporciona una vista 24x7 de la actividad del tráfico global para analizar anomalías, descubrir nuevas amenazas y monitorear las tendencias del tráfico. Talos previene los ataques de hora 
                                    cero al generar continuamente nuevas reglas que alimentan actualizaciones a la WSA cada tres a cinco minutos, lo que permite una defensa contra amenazas líder en la industria horas e incluso días antes 
                                    que los competidores.</p>

                                <h4>Controles de uso de la Web de Cisco</h4>
                                <p align="justify">Combine el filtrado de URL tradicional con el análisis de contenido dinámico para mitigar los riesgos de cumplimiento, responsabilidad y productividad. La base de datos de filtrado de URL 
                                    continuamente actualizada de Cisco de más de 50 millones de sitios bloqueados proporciona una cobertura excepcional para sitios web conocidos, y el motor de análisis de contenido dinámico (DCA) identifica 
                                    con precisión el 90 por ciento de las URL desconocidas en tiempo real; escanea el texto, califica el texto para determinar su relevancia, calcula la proximidad del documento del modelo y devuelve la categoría 
                                    más cercana. Los administradores también pueden seleccionar categorías específicas para la inspección HTTPS inteligente.</p>

                                <h4>Protección avanzada contra malware</h4>
                                <p align="justify">La protección avanzada contra malware (AMP) es una función con licencia adicional disponible para todos los clientes de Cisco WSA. AMP es una solución integral de eliminación de malware 
                                    que permite la detección y el bloqueo de malware, el análisis continuo y las alertas retrospectivas. Aprovecha las vastas redes de inteligencia de seguridad en la nube de la tecnología Cisco y Sourcefire®. 
                                    AMP aumenta las capacidades de detección y bloqueo de malware que ya se ofrecen en Cisco WSA con capacidades mejoradas de reputación de archivos, informes detallados del comportamiento de los archivos, 
                                    análisis continuo de archivos y alertas de veredicto retrospectivo. Cisco AMP Threat Grid ofrece protección contra malware a través de un dispositivo local para organizaciones que tienen restricciones 
                                    de cumplimiento o políticas para enviar muestras de malware a la nube. El Monitor de tráfico de capa 4 escanea continuamente la actividad, detectando y bloqueando las comunicaciones de "teléfono-hogar" 
                                    de software espía. Al rastrear todas las aplicaciones de red, el Monitor de tráfico de capa 4 detiene eficazmente el malware que intenta eludir las soluciones clásicas de seguridad web. Agrega dinámicamente 
                                    direcciones IP de dominios de malware conocidos a su lista de entidades maliciosas para bloquear.</p>

                                <h4>Análisis cognitivo de amenazas</h4>
                                <p align="justify">Cisco Cognitive Threat Analytics es una solución basada en la nube que reduce el tiempo de descubrimiento de amenazas que operan dentro de la red. Aborda las brechas en las defensas basadas 
                                    en el perímetro al identificar los síntomas de una infección de malware o una violación de datos mediante el análisis de comportamiento y la detección de anomalías. Aproveche Cisco Cognitive Threat 
                                    Analytics con una simple licencia complementaria para su solución de seguridad web. Reduzca la complejidad mientras obtiene una protección superior que evoluciona con su cambiante panorama de amenazas. </p>

                                <h4>Visibilidad y control de aplicaciones (AVC)</h4>
                                <p align="justify">Controle fácilmente el uso de cientos de aplicaciones Web 2.0 y más de 150.000 microaplicaciones. El control de políticas granular permite a los administradores permitir el uso de aplicaciones 
                                    como Dropbox o Facebook mientras bloquea a los usuarios de actividades como cargar documentos o hacer clic en el botón "Me gusta". La WSA admite la visibilidad de la actividad en toda una red. Nuevo: los 
                                    clientes pueden implementar cuotas personalizadas de ancho de banda y tiempo por usuario, por grupo y por política. </p>                                                 

                                <h4>Prevención de pérdida de datos (DLP)</h4>
                                <p align="justify">Evite que los datos confidenciales salgan de la red creando reglas basadas en el contexto para DLP básico. Cisco WSA también utiliza el Protocolo de adaptación de contenido de Internet 
                                    (ICAP) para integrarse con soluciones DLP de terceros para una inspección profunda del contenido y la aplicación de las políticas DLP. Cisco WSA también admite Secure ICAP para cifrar el tráfico 
                                    intercambiado entre WSA y las soluciones DLP de terceros. </p>
                                    
                                <h4>Protección de usuarios itinerantes</h4>
                                <p align="justify">Cisco WSA protege a los usuarios de roaming al integrarse con Cisco AnyConnect® Secure Mobility Client, que brinda seguridad web a clientes remotos al iniciar un túnel VPN que redirige 
                                    el tráfico de regreso a la solución local. La tecnología Cisco AnyConnect analiza el tráfico en tiempo real antes de permitir el acceso. </p>

                                <p align="justify">Cisco WSA también está integrado con Cisco Identity Services Engine (ISE). Con esta interesante mejora, los clientes ahora pueden aprovechar el poder de Cisco ISE para Cisco WSA si lo 
                                    solicitan. La integración de Cisco ISE permite a los administradores crear políticas en Cisco WSA en función de la información de perfil o membresía recopilada por Cisco ISE a través de su proceso de 
                                    inicio de sesión único. </p>
                                
                                <h4>Detección de correo gris y cancelación segura de la suscripción</h4>
                                <p align="justify">Graymail consiste en marketing, redes sociales y mensajes masivos. La función de detección de correo gris clasifica y monitorea con precisión el correo gris que ingresa a una organización. 
                                    Luego, un administrador puede tomar las medidas adecuadas en cada categoría. A menudo, el correo gris tiene un enlace para cancelar la suscripción donde los usuarios finales pueden indicar al remitente 
                                    que les gustaría optar por no recibir dichos correos electrónicos. Dado que imitar un mecanismo de cancelación de suscripción es una técnica de phishing popular, los usuarios deben tener cuidado al 
                                    hacer clic en estos enlaces de cancelación de suscripción.</p>

                                <h4>Gestión e informes centralizados </h4>
                                <p align="justify">Reciba información útil sobre amenazas, datos y aplicaciones. Cisco WSA proporciona una herramienta de administración centralizada y fácil de usar para controlar operaciones, administrar 
                                    políticas y ver informes.</p>

                                <p align="justify">El dispositivo de administración de seguridad de contenido de la serie M de Cisco proporciona administración e informes centrales en múltiples dispositivos y múltiples ubicaciones, 
                                    incluidas las instancias virtuales.</p>

                                <p align="justify">La aplicación de informes de seguridad web de Cisco® es una solución de informes que indexa y analiza rápidamente los registros producidos por los dispositivos de seguridad web de Cisco 
                                    (WSA) y la seguridad web de la nube de Cisco (CWS). Esta herramienta proporciona informes escalables para clientes con grandes necesidades de almacenamiento y tráfico. Permite a los administradores 
                                    de informes recopilar información detallada sobre el uso de la web y las amenazas de malware. </p>
                                
                                <h4>Implementación flexible </h4>
                                <p align="justify">Cisco WSAV ofrece las mismas funciones que Cisco WSA, con la conveniencia y el ahorro de costos adicionales de un modelo de implementación virtual, incluido el aprovisionamiento 
                                    instantáneo de autoservicio. Con una licencia de Cisco WSAV, las empresas pueden implementar puertas de enlace virtuales de seguridad web sin estar conectadas a Internet, aplicando la licencia 
                                    a un nuevo archivo de imagen virtual de Cisco WSAV almacenado localmente. Los archivos de imágenes virtuales prístinos se pueden clonar, si es necesario, para implementar varias puertas de 
                                    enlace de seguridad web de inmediato. </p>
                                <p align="justify">Ejecute hardware y máquinas virtuales en la misma implementación. Las sucursales pequeñas o las ubicaciones remotas pueden tener la misma protección que proporciona Cisco WSA 
                                    sin tener que instalar y admitir hardware en esa ubicación. La implementación personalizada se administra fácilmente con el dispositivo de administración de seguridad de contenido de la serie M de Cisco. </p>

