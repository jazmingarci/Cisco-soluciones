                            <h2>Descripción general:</h2>
                            <p align="justify">Los routers VPN Cisco Small Business RV260, RV260P y RV260W son modelos de alto rendimiento que combinan características de clase empresarial con rendimiento, seguridad, confiabilidad y valor general a un excelente precio. Estos modelos son perfectos para la red de pequeñas empresas, pequeñas empresas, sucursales o pequeñas oficinas en el hogar.</p>
                            <p align="justify">Fáciles de configurar e instalar, los routers RV260 se pueden implementar de forma remota mediante Plug and Play (PnP), son compatibles con la plataforma de red Cisco FindIT y cuentan con una nueva interfaz de usuario.</p>
                            <p align="justify">Estos modelos tienen un procesador de doble núcleo que proporciona un rendimiento que normalmente solo se encuentra en soluciones más caras. Un puerto WAN conectable de factor de forma pequeño (SFP) proporciona conectividad flexible y protección de la inversión. Al igual que los otros modelos de la cartera, el RV260, 260P y 260W incluyen características estándar como un firewall de inspección de paquetes de estado (SPI) de nivel empresarial, seguridad VPN (seguridad IP [IPsec], protocolo de túnel punto a punto [PPTP ] y OpenVPN), filtrado de contenido y asistentes de dispositivos para facilitar aún más la configuración.</p>
                            <p align="justify">El RV260 con cable ofrece una experiencia de usuario de alto rendimiento a un precio atractivo. El RV260P agrega Power over Ethernet (PoE) a cuatro de los ocho puertos. El RV260W agrega 802.11ac inalámbrico 3x3. Al igual que el RV160W, pero con mejor rendimiento y más puertos, este network-in-a-box ofrece un rendimiento y alcance inalámbricos fantásticos para la solución de red de pequeñas empresas y teletrabajadores.</p>
