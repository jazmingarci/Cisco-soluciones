                                            <li>La infraestructura Cisco Prime ™ proporciona una gestión integral del ciclo de vida de la red con una amplia biblioteca de funciones que automatizan
                                                 la gestión inicial y diaria. Cisco Prime integra la experiencia operativa y la experiencia en plataformas de hardware y software en un poderoso 
                                                 conjunto de herramientas de configuración, supervisión, resolución de problemas, generación de informes y administrativas impulsadas por el flujo 
                                                 de trabajo </li>

                                            <li>Cisco Network Assistant es una aplicación de configuración y administración de red centralizada basada en PC para pequeñas y medianas empresas (SMB) 
