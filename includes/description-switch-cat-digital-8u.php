                                            <b>Configuración a escala</b> <p align="jusify">
                                            El conmutador está preparado para Cisco DNA y se puede utilizar como parte de la solución APIC-EM para implementaciones automatizadas de conmutadores. También es 
                                            compatible con Network Plug-n-Play (PnP), una solución segura y escalable que acelera las implementaciones de dispositivos de red al automatizar la instalación / 
                                            configuración del software Cisco IOS, mejorar la productividad y la experiencia del usuario y reducir los costos y el tiempo de inactividad. </p>
                                        
                                    
                                            <b>Actualizaciones remotas </b> <p align="jusify">
                                            El conmutador permite actualizaciones, mantenimiento y resolución de problemas sin problemas para la iluminación y otras redes de edificios mediante el cambio de 
                                            software por aire . Esto se mejora con las sencillas actualizaciones de configuración y firmware fuera de línea con una tarjeta SD que utiliza una interfaz de 
                                            aplicación móvil intuitiva </p>
                                       
