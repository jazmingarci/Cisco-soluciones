                                                permite la identificación rápida de amenazas a la seguridad. </li>
                                            <li>Red como ejecutor: junto con Cisco Catalyst 3850 Series como conmutador ascendente, las capacidades de creación de perfiles de dispositivos y Red como 
                                                ejecutor (NaaE) están habilitadas para hacer cumplir dinámicamente la seguridad basada en roles para reducir la superficie de ataque general, contener 
                                                ataques y minimizar el tiempo necesario para aislar las amenazas cuando se detectan mediante Cisco TrustSec® con Cisco Identity Services Engine (ISE). </li>
                                            
                                        </ul> 

