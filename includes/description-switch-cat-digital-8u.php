                                <h2>Caracteristicas y Beneficios:</h2>
                                    <p align="justify">El conmutador Cisco Catalyst Digital Building Series es el primer conmutador de la industria especialmente diseñado y optimizado para 
                                        alimentar sistemas de iluminación LED y digitales de bajo voltaje con muchas características únicas. A diferencia de los conmutadores 
                                        de IoT no administrados, proporciona funciones de red avanzadas para brindar flexibilidad, simplicidad, seguridad y escala. </p>   
                                    
                                    <h4><strong>Cambiar la confiabilidad y la escala </strong></h4>  
                                        <p align="jusify">
                                            <strong>Factor de forma pequeño, diseño sin ventilador, funcionamiento silencioso </strong>- 
                                                    El primer y único conmutador sin ventilador semirresistente de la industria, que ofrece hasta 60 W de potencia / puerto, 
                                                    ideal para su implementación en espacios de trabajo abiertos en interiores y otras áreas que no requieren ruido de equipo.</p>
                                        
                                        
                                            <b>Mayor escala de PoE </b> <p align="jusify">
                                            Proporciona una escala de PoE sin precedentes, hasta 480 W de UPOE doble (el del presupuesto de energía en cualquier conmutador sin ventilador) 
                                            y 240 W de PoE + </p>

                                        
                                            <b>UPOE perpetuo y rápido </b> <p align="jusify">
                                            El primer UPOE perpetuo de la industria garantiza la alimentación ininterrumpida de los puntos finales durante la actualización del conmutador,
                                             los reinicios y los cambios de configuración. La UPOE rápida garantiza la alimentación de los puntos finales PoE y UPOE dentro de los posteriores 
                                             a la 5 segundos restauración de la energía, en caso de pérdidas de energía </p>

                                        
                                            <b>Vida del sistema </b> <p align="jusify">
                                            Este interruptor viene con la la confiabilidad más alta de industria y 10 años de vida útil del sistema. </p>

                                       
                                            <b>Arquitectura prevalidada </b>  <p align="jusify">
                                            Cisco admite soluciones de iluminación probadas, validadas e integradas, que pueden reducir el riesgo de implementación y acelerar el tiempo 
                                            de generación de valor. La tecnología robusta probada, la red existente y la seguridad de un extremo a otro proporcionan una solución de 
                                            construcción digital completa </p>

                                    <h4><strong>Simplicidad de instalación </strong></h4>
                                       
                                            <b>Opciones de montaje flexibles </b>  <p align="jusify">
                                            Admite opciones de instalación / montaje flexibles, incluidos bastidor, armario eléctrico y en el techo </p>
                                        
                                       
                                            <b>Opciones de alimentación flexibles </b>  <p align="jusify">
                                            El interruptor se puede alimentar a través de cables directos para aplicaciones de 100-277 VCA o con enchufes IEC para entradas de voltaje de 100-240 VCA </p>

                                        
                                            <b>Pruebas automatizadas en implementación </b> <p align="jusify">
                                            Los valores predeterminados inteligentes y la aplicación móvil integrada (disponible en Android e iOS) reducen en gran medida el esfuerzo de probar la implementación 
                                            por parte de instaladores no capacitados en TI, lo que resulta en menores costos de instalación. </p>
                                    

