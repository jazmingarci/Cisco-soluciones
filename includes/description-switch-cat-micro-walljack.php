                                    <h4><strong>Gestión de energía </strong></h4>

                                            <b>Cambiar el modo de hibernación y sueño profundo</b>
                                            <p align="justify">La gestión del sueño del conmutador se puede iniciar a través de Cisco EnergyWise®, como se hacía tradicionalmente, o mediante CoAP. La compatibilidad 
                                                con CoAP en los microconmutadores Catalyst habilita un activador de suspensión. Este soporte se proporciona para iniciar la suspensión con la configuración 
                                                del reloj en tiempo real (RTC). El sistema puede activarse en función de un botón de modo y un reloj de activación. </p>

                                            <b>IEEE 802.3az o Ethernet de bajo consumo energético (EEE) </b>
                                            <p align="justify">Los puertos detectan dinámicamente los períodos de inactividad entre las ráfagas de tráfico y cambian rápidamente las interfaces a un modo 
                                                inactivo de bajo consumo de energía, lo que reduce el consumo de energía.</p>

                                            <b>Eficiencia energética del sistema </b>
                                            <p align="justify">Los interruptores también mejoran el estándar de la industria en cuanto a eficiencia energética del sistema. Durante la condición de sueño, 
                                                el sistema está reduciendo su uso de energía en un 65%, mejorando su eficiencia energética. </p>

                                            <b>Administrador de energía de Cisco  </b>
                                            <p align="justify">Integrado con la suite Cisco Energy Management para un control total de la energía y visibilidad. </p>

