                                <h2>Switches Cisco Catalyst Micro Series: </h2>
                                <p align="justify">Los microconmutadores Cisco Catalyst son la familia de productos diseñados específicamente por Cisco para implementaciones 
                                    FTTx, que permiten a los clientes eliminar IDF, ahorrar valiosos bienes inmuebles, conservar energía y reducir los costos de cableado. </p>
                                
                                <p align="justify">La fibra para el escritorio, la oficina, etc. (FTTx) no es un concepto nuevo. En este escenario de implementación, la gama 
                                    mejorada de fibra óptica sobre cables de cobre permite que los interruptores de acceso se implementen junto a los dispositivos finales, 
                                    con enlaces ascendentes conectados directamente al marco de distribución principal, eliminando la necesidad de un marco de distribución 
                                    intermedio en cada piso. Sin embargo, a medida que los conmutadores de acceso se acercan a los usuarios finales, los requisitos de ruido 
                                    de funcionamiento y la huella del producto se vuelven más estrictos. Ingrese a Cisco Catalyst Micro Switches, una familia de productos 
                                    de pequeños switches sin ventilador especialmente diseñados para implementaciones FTTx. Estos conmutadores ofrecen opciones de montaje 
                                    flexibles y abren una variedad de oportunidades de conectividad y diseño de red. </p>

                                <ul>
                                    <li>Cuatro puertos Gigabit Ethernet y dos enlaces ascendentes de cobre o fibra Gigabit Ethernet, con rendimiento de reenvío de velocidad de línea </li>
                                    <li>Compatibilidad con Power over Ethernet Plus (PoE +), con un presupuesto de energía PoE de hasta 60 W en conmutadores de enchufe de pared y 
                                        hasta 120 W para versiones de escritorio </li>
                                    <li>Diseño sin ventilador para un funcionamiento silencioso y una mayor fiabilidad </li>
                                    <li>Garantía limitada de por vida mejorada (E-LLW) </li>
                                </ul>

