                                                automatizan la gestión inicial y diaria. Cisco Prime integra la experiencia operativa y la experiencia en plataformas de hardware y software en un 
                                                poderoso conjunto de herramientas de configuración, supervisión, resolución de problemas, generación de informes y administrativas impulsadas por 
