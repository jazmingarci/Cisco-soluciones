                                <h2>Caracteristicas y Beneficios:</h2>
                                    <h4><strong>Red óptica pasiva</strong></h4>  
                                        <ul>
                                            <li>El elemento pasivo es estable, fácil de mantener sin fallas. </li>
                                            <li>All-Optical tiene un alto ancho de banda y un gran potencial para la actualización del ancho de banda en el futuro, que es la 
                                                tendencia de desarrollo de la red portadora. </li>
                                            <li>Red simple, equilibrio de carga y administración de red de un extremo a otro para un fácil mantenimiento. </li>
                                        </ul>   
                                    
                                    <h4><strong>Alto rendimiento </strong></h4>  
                                        <ul>
                                            <li>La plataforma adopta una red totalmente óptica, que tiene una gran capacidad de ancho de banda para transportar múltiples 
                                                servicios. Tiene una buena capacidad de expansión y un espacio de actualización de la red, que está en línea con la tendencia 
                                                de desarrollo de la red. </li>
                                            <li>Gran ancho de banda, el ancho de banda máximo de un solo usuario puede alcanzar los 2500 Mbps. </li>
                                            <li>Una fibra que transporta múltiples servicios, soporta voz, video, seguridad, Internet y otros servicios. </li>
                                        </ul>  

                                    <h4><strong>Alta fiabilidad </strong></h4>  
                                        <ul>
                                            <li>Admite protección de tipo B, respaldo y protección de recuperación de desastres para el puerto PON. </li>
                                            <li>Redundancia de energía y enrutamiento de enlace ascendente dual para evitar un solo punto de falla de la red.</li>
                                            <li>IEEE 802.1w Rapid Spanning Tree Protocol (RSTP) proporciona una rápida convergencia del árbol de expansión independiente 
                                                de los temporizadores del árbol de expansión y también ofrece el beneficio del procesamiento distribuido y el equilibrio de carga de Capa 2. </li>
                                            <li>El protocolo de control de agregación de enlaces (LACP) IEEE802.3AD proporciona enlaces de enlaces múltiples para lograr una copia de seguridad 
                                                del enlace ascendente.</li>
                                        </ul>  

                                    <h4><strong>QoS</strong></h4>  
                                        <ul>
                                            <li>La plataforma ofrece garantías de calidad de servicio de un extremo a otro. A través de la aplicación integral de gestión de congestión, 
                                                marcado de prioridades, programación de colas y limitación / configuración de la tasa de tráfico, puede cumplir con los requisitos de 
                                                calidad de servicio del usuario y crear una red de alta calidad para los clientes. </li>
                                            <li>Garantía de calidad del servicio: según el protocolo IEEE 802.1p, se establecen diferentes valores de CoS (Clase de servicio) de clase de 
                                                servicio de acuerdo con los diferentes tipos de servicio, y se proporcionan métodos de procesamiento de prioridad diferenciados para mejorar 
                                                el índice de calidad de la red, como el tiempo de retardo, fluctuación, y tasa de pérdida de paquetes. Diferentes clasificaciones de tráfico 
                                                tienen diferentes prioridades, y diferentes colas como SP (Prioridad estricta) y WRR (Ronda ponderada) se utilizan para realizar la programación 
                                                de la cola de los flujos de datos para mejorar la calidad de los servicios.</li>
                                            <li>Garantía de ancho de banda: El ancho de banda del usuario está garantizado por la aplicación del mecanismo DBA (asignación dinámica de ancho de banda) 
                                                y la función de limitación / modelado de la tasa de tráfico. Generalmente, el servicio de VoIP se establece en un ancho de banda fijo, los servicios 
                                                de video e IPTV se configuran para garantizar el ancho de banda y el servicio de Internet de alta velocidad se establece en el ancho de banda máximo. 
                                                El flujo de datos está limitado y conformado por una pluralidad de plantillas de línea para garantizar la garantía de ancho de banda. </li>
                                        </ul> 

                                                                            
                                    <h4><strong>Instalación e implementación rápidas</strong></h4>  
                                        <ul>                                    
                                            <li>Configuración y entrega automática de datos, apertura rápida del servicio. </li>
                                            <li>Equipos de bajo consumo energético para ahorrar energía, reducir emisiones y proteger el medio ambiente. </li>
                                        </ul> 

                                    <h4><strong>Facil mantenimiento </strong></h4>  
                                        <ul>                                    
                                            <li>Todo el proceso de conexión óptica pasiva hace que la topología de la red sea estable y confiable, y el punto de falla se reduce considerablemente. 
                                                El sistema de gestión de red puede gestionar directamente los puertos de usuario de la ONT, proporcionar garantías de servicio de QoS de extremo a 
                                                extremo para toda la red e implementar la gestión de red de extremo a extremo, lo que reduce la presión y la intensidad de operación y mantenimiento, 
                                                mientras mejora la eficiencia de operación y mantenimiento. </li>
                                            <li>Cisco PON Manager es una herramienta de administración de dispositivos basada en GUI integrada que brinda la capacidad de aprovisionar el dispositivo, 
                                                simplificar la implementación y la capacidad de administración del dispositivo y mejorar la experiencia del usuario. Admite alarmas, rendimiento, 
                                                configuración y entrega de datos, seguridad, registro, gestión de terminales y funciones de actualización por lotes de software. </li>
                                        </ul> 
                                    
