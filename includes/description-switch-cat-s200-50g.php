                                <h2>Características y Beneficios: </h2>
                                <p align="justify">Los switches Cisco de la serie 200 brindan todas las funciones que necesita para crear una red básica de clase 
                                    empresarial a un precio asequible. Estas características incluyen: </p>

                                    <h4><strong>Configuración y administración sencillas</strong></h4>  
                                        <p align="justify"> los switches Cisco de la serie 200 están diseñados para que las pequeñas empresas o los socios que les 
                                            brinden servicios puedan implementarlos y usarlos fácilmente. Las interfaces basadas en web fáciles de usar reducen el 
                                            tiempo que lleva implementar, administrar y solucionar problemas de su red. Las características clave incluyen: </p> 

                                            <ul>
                                                <li><p align="justify">Cisco Discovery Protocol simplifica la configuración al descubrir todos los dispositivos Cisco conectados y 
                                                    permitirles compartir información </p></li>
                                                <li><p align="justify">La utilidad de descubrimiento de red Cisco FindIT funciona a través de una barra de herramientas simple en 
                                                    el navegador web del usuario para descubrir dispositivos Cisco en la red y mostrar información básica, como 
                                                    números de serie y direcciones IP, para ayudar en la configuración y acelerar la implementación de productos 
                                                    Cisco Small Business. Para obtener más información y descargar la utilidad, visite www.cisco.com/go/sbtoolbar.</p></li>
                                            </ul>
                                    
                                    <h4><strong>Rendimiento y confiabilidad</strong></h4>  
                                        <p align="justify">los switches Cisco de la serie 200 se han probado para ofrecer la alta disponibilidad y el rendimiento 
                                            que esperaría de un switch Cisco y ayudarlo a evitar costosos tiempos de inactividad. Los conmutadores aceleran los 
                                            tiempos de transferencia de archivos, mejoran las redes lentas y lentas, mantienen disponibles sus aplicaciones comerciales 
                                            vitales y ayudan a sus empleados a responder más rápidamente a los clientes y entre ellos. Con una red basada en switches 
                                            Cisco de la serie 200, puede abordar todas sus necesidades de conectividad y comunicación empresarial y reducir el 
                                            costo total de propiedad de su infraestructura tecnológica.  </p> 

                                    <h4><strong>PoE</strong></h4>  
                                        <p align="justify">Los switches Cisco de la serie 200 están disponibles con PoE en los modelos Fast Ethernet y Gigabit 
                                            Ethernet. Esta capacidad simplifica la implementación de telefonía IP, inalámbrica, videovigilancia y otras soluciones 
                                            al permitirle enviar datos y energía a los puntos finales de la red a través del mismo cable de red. Sin necesidad de 
                                            fuentes de alimentación o enchufes separados para teléfonos IP, cámaras IP o puntos de acceso inalámbricos, puede 
                                            acelerar la implementación y la instalación y aprovechar las tecnologías de comunicaciones avanzadas rápidamente y a 
                                            un costo menor.  </p> 

                                    <h4><strong>Seguridad de la red</strong></h4>  
                                        <p align="justify">Los switches Cisco de la serie 200 brindan funciones básicas de seguridad y administración de red que necesita 
                                            para mantener un nivel de seguridad para su empresa, mantener a los usuarios no autorizados fuera de la red y proteger 
                                            sus datos comerciales. Los conmutadores brindan seguridad de red integrada para reducir el riesgo de una brecha de 
                                            seguridad, con seguridad de puerto IEEE 802.1X para controlar el acceso a su red.</p> 

                                    <h4><strong>Compatibilidad con telefonía IP</strong></h4>  
                                        <p align="justify">Los switches Cisco de la serie 200 incluyen funciones de QoS para priorizar los servicios sensibles 
                                            a los retrasos, como voz y video, simplificar las implementaciones de comunicaciones unificadas y ayudar a garantizar 
                                            un rendimiento constante de la red para todos los servicios. Por ejemplo, las capacidades de Auto Voice VLAN le 
                                            permiten conectar cualquier teléfono IP (incluidos los teléfonos de terceros) a su red de telefonía IP y recibir 
                                            un tono de marcación inmediato. El conmutador configura automáticamente el dispositivo con los parámetros de VLAN 
                                            y QoS correctos para priorizar el tráfico de voz. </p> 

                                    <h4><strong>Compatibilidad con IPv6</strong></h4>  
                                        <p align="justify">Los proveedores de servicios de Internet de todo el mundo están adoptando la última versión del 
                                            Protocolo de Internet para adaptarse al creciente número de dispositivos de red. Los switches Cisco de la serie 
                                            200 brindan soporte nativo para IPv6 junto con IPv4 tradicional. Eso significa que puede aprovechar al máximo los 
                                            sistemas operativos y aplicaciones habilitados para IPv6 en el futuro, sin tener que actualizar su equipo de red. </p> 

                                    <h4><strong>Una solución de eficiencia energética</strong></h4>  
                                        <p align="justify">Los switches Cisco de la serie 200 están diseñados para ser ecológicos sin comprometer el rendimiento. 
                                            Ayudan a conservar energía al optimizar el uso de energía, lo que ayuda a proteger el medio ambiente y reduce sus 
                                            costos de energía. Las características de ahorro de energía incluyen: </p> 
                                            <ul>
                                                <li><p align="justify">Apagado automático en puertos Gigabit cuando un enlace no está activo</p></li>
                                                <li><p align="justify">Inteligencia incorporada para ajustar la energía según la longitud del cable en modelos Gigabit Ethernet</p></li>
                                                <li><p align="justify">Diseño sin ventilador en la mayoría de los modelos, que reduce el consumo de energía, aumenta la confiabilidad 
                                                    y proporciona un funcionamiento más silencioso</p></li>
                                            </ul>

                                    
                                    <h4><strong>Puertos Gigabit Ethernet adicionales</strong></h4>  
                                        <p align="justify">La serie 200 de Cisco proporciona más puertos por conmutador que otros conmutadores del mercado, lo que 
                                            le brinda más flexibilidad para conectarse y potenciar su negocio. Los modelos Gigabit Ethernet cuentan con conmutadores 
                                            de 26 y 50 puertos, en comparación con los dispositivos tradicionales que ofrecen 20 o 44 puertos con 4 puertos 
                                            compartidos. La serie 200 de Cisco también ofrece ranuras de expansión mini Gigabit Interface Converter (mini-GBIC) 
                                            que le brindan la opción de agregar conectividad de enlace ascendente de fibra óptica o Gigabit Ethernet al conmutador. 
                                            Con la capacidad de aumentar el rango de conectividad de los conmutadores, tiene más flexibilidad para diseñar su red 
                                            en torno a su entorno empresarial único y para conectar fácilmente conmutadores en diferentes plantas o en toda la 
                                            empresa. </p> 

                                    <h4><strong>Tranquilidad y protección de la inversión</strong></h4>  
                                        <p align="justify">Los switches Cisco de la serie 200 ofrecen el rendimiento confiable, la protección de la inversión y la 
                                            tranquilidad que espera de un switch Cisco. Cuando invierte en Cisco 200 Series, obtiene el beneficio de: </p>
                                            <ul>
                                                <li><p align="justify">Garantía limitada de por vida de Cisco</p></li>
                                                <li><p align="justify">Programa de protección de inversiones de Cisco Small Business, que le permite actualizar su conmutador Cisco 
                                                    de la serie 200 a otro conmutador Cisco Small Business o Cisco Catalyst® en el futuro y recibir crédito por el 
                                                    valor del conmutador (disponible solo en los Estados Unidos y Canadá)</p></li>
                                                <li><p align="justify">Pruebas rigurosas para ayudar a garantizar una fácil integración y compatibilidad con otros productos de 
                                                    comunicaciones y redes de Cisco, incluida la cartera completa de Cisco Small Business</p></li>
                                            </ul>
                                            
                                    <h4><strong>Servicio y soporte de clase mundial</strong></h4>  
                                        <p align="justify">Su tiempo es valioso, especialmente cuando tiene un problema que está afectando su negocio. Es por eso 
                                            que los switches Cisco de la serie 200 son compatibles con Cisco Small Business Support Center, un recurso dedicado 
                                            para redes y clientes de pequeñas empresas. El Centro de soporte de Cisco Small Business lo conecta directamente con 
                                            un técnico que obtuvo la certificación Cisco CCNA®, quien puede brindarle la ayuda experta que necesita de inmediato. 
                                            También tiene acceso a una amplia información técnica y de productos a través de la comunidad de soporte de Cisco 
                                            Small Business. Visite www.cisco.com/go/smallbizsupport</p> 

                                    <h4><strong>Múltiples opciones de idioma</strong></h4>  
                                        <p align="justify">Cisco 200 Series está disponible en siete idiomas: inglés, francés, alemán, italiano, español, japonés 
                                            y chino simplificado. Toda la documentación del producto y la mayoría de las interfaces de usuario están traducidas, 
                                            lo que le permite seleccionar su idioma preferido.</p> 
