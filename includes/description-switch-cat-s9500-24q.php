                                <h4><strong>Serie Catalyst 9500 con Cisco DNA </strong></h4>  <img class="img-responsive" src="images/switches/catalyst-9500.png" align="right">                              
                                <p align="justify">Cisco Catalyst 9500 Series es la primera línea de Ethernet de 25, 40 y 100 Gigabit especialmente 
                                    diseñada de la industria. conmutadores destinados al campus empresarial. Estos conmutadores ofrecen una escala 
                                    de tabla inigualable (MAC / ruta / ACL) y almacenamiento en búfer para aplicaciones empresariales. Cisco Catalyst 
                                    9500 Series incluye 40 y 100 sin bloqueo Gigabit Ethernet cuádruple de factor de forma pequeño conectable (QSFP 
                                    +, QSFP28) y 1, 10 y 25 Gigabit Ethernet pequeño Switches de factor de forma Pluggable Plus (SFP / SFP + / SFP28) 
                                    con densidades de puertos granulares que se adaptan a diversos campus necesidades. <br>
                                La Arquitectura de red digital de Cisco ( Cisco DNA ) con acceso definido por software (SD-Access) 
                                    es la red tejido que impulsa los negocios. Es una arquitectura abierta y extensible, impulsada por software que 
                                    acelera y simplifica las operaciones de su red empresarial. La arquitectura programable libera a su personal de 
                                    TI de las tareas de configuración de red repetitivas y que consumen mucho tiempo para que puedan concentrarse 
                                    en la innovación. </p>
                                    

