                            <p align="justify">En el dinámico entorno empresarial actual, los empleados se vuelven más móviles y colaborativos que nunca. Para mantenerse productivos, necesitan un acceso confiable y de clase empresarial a las aplicaciones de red en toda la oficina. El punto de acceso de radio dual Cisco® WAP131 Wireless-N ofrece una forma simple y rentable de extender la red móvil segura y de alto rendimiento a sus empleados e invitados, para que puedan permanecer conectados en cualquier lugar de la oficina. Esta solución flexible le permite conectar a docenas de empleados y puede escalar para adaptarse a usuarios adicionales y necesidades comerciales cambiantes. </p>

