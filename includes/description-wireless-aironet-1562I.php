                                <h2>Característica destacada/diferenciador/capacidad</h2> 
                                <p> La serie Cisco Aironet 1560 ofrece las siguientes características:</p>
                                <ul> 
                                  <li>Rendimiento mejorado para varios dispositivos cliente: los puntos de acceso 802.11ac Wave 2 utilizan la tecnología MU-MIMO, que permite que diferentes flujos de datos fluyan a la vez desde el punto de acceso a varios dispositivos compatibles con 802.11ac Wave 2. Ahora, múltiples dispositivos 802.11ac Wave 2 pueden conectarse al mismo tiempo, obteniendo la información que necesitan más rápido.</li>
                                  <li>Compatibilidad con 5 GHz: la serie Cisco Aironet 1560 duplica la escala de los dispositivos móviles de 5 GHz y aumenta el rendimiento de los entornos de alta densidad.</li>
                                  <li>Coexistencia 4G LTE: la serie Cisco Aironet 1560 incluye un filtrado sólido en la banda sin licencia de 2,4 GHz para bloquear las señales celulares cercanas 4G LTE con licencia.</li>
                                  <li>La tecnología de puerto de antena flexible de Cisco utiliza un software configurable para antenas de banda única o doble. Le permite usar los mismos puertos de antena para antenas de doble banda para reducir el espacio ocupado o antenas de banda única para optimizar la cobertura de radio.</li>
                                  <li>Cisco Mobility Express: esta solución está diseñada para brindar acceso inalámbrico de clase empresarial a redes pequeñas y medianas. Fácil de configurar con poco mantenimiento, Mobility Express incluye funciones avanzadas de Cisco y no requiere un dispositivo controlador físico.</li>
                                  <li>Cisco High Density Experience (HDX): Cisco HDX viene de serie en el 1560, lo que brinda a este punto de acceso una eficiencia de red de primer nivel en una gran cantidad de clientes inalámbricos. HDX utiliza conjuntos de chips personalizados para satisfacer las necesidades de las redes de alta densidad. Está construido con la mejor arquitectura de RF de su clase y brinda una mejor experiencia de usuario para aplicaciones de alto rendimiento.</li>
                                </ul>
                           
