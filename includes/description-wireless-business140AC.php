                                <h2>Característica destacada</h2> 
                                <h3>Mayor rendimiento y cobertura inalámbricos, con tecnología de malla</h3>
                                <p align="justify">El punto de acceso Cisco Business 140AC es compatible con el último estándar 802.11ac Wave 2 para un mayor rendimiento, mayor cobertura y redes de mayor densidad. Con radios duales y bandas duales simultáneas con funcionalidad 802.11ac Wave 2 MU-MIMO, este punto de acceso puede manejar el creciente número de dispositivos y aplicaciones de alto ancho de banda que pronto se convertirán en una parte común de la red.
                                <br>
                                <p align="justify">La tecnología de malla integrada permite una flexibilidad de implementación que es simple y segura. Puede expandir fácilmente la cobertura y la capacidad con los extensores de malla inalámbricos de Cisco Business, que incluyen modelos que se pueden colocar en un escritorio, enchufar a un tomacorriente de CA o montar directamente en una pared.
                                <h3>Alimentación a través de Ethernet</h3>
                                <p align="justify">El punto de acceso Cisco Business 140AC cuenta con un solo puerto de enlace ascendente Gigabit Ethernet y funciona con alimentación a través de Ethernet (estándar PoE 802.3af).
                                <h3>Montaje</h3>
                                <p align="justify">Este elegante y compacto punto de acceso está diseñado con opciones de montaje flexibles en mente. Puede montarlo directamente en el techo (incluido el montaje en riel de barra en T) o en una pared. Es fácil de instalar y el hardware de montaje está incluido.
                           
