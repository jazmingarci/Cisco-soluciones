                                <h2>Característica destacada</h2> 
                                <h3>Mayor rendimiento y cobertura inalámbricos, con tecnología de malla</h3>
                                <p align="justify">El access point Cisco Business 145AC es compatible con el último estándar 802.11ac Wave 2 para un mayor rendimiento, mayor acceso y redes de mayor densidad. Con radios duales y bandas duales simultáneas con funcionalidad 802.11ac Wave 2 MU-MIMO, este punto de acceso puede manejar el creciente número de dispositivos de gran ancho de banda que pronto se convertirán en una parte común de la red.
                                <h3>Aplicación móvil empresarial de Cisco</h3>
                                <p align="justify">El access point Cisco Business 145AC es administrado por la aplicación Cisco Business Mobile, una aplicación de cliente intuitiva que simplifica los desafíos tradicionales asociados con la implementación de redes inalámbricas, directamente desde su dispositivo móvil. Puede ampliar su red agregando nuevos extensores de malla y automatizando la activación de la red inalámbrica en minutos. Una capacidad de administración robusta cambia dinámicamente la configuración de la red, como permitir el acceso de invitados. El uso de la red, los patrones de tráfico y el rendimiento de la red se pueden monitorear de cerca, proporcionando una instantánea en tiempo real; esto no solo proporciona tranquilidad, sino que prácticamente garantiza una experiencia de usuario óptima. La aplicación Cisco Business Mobile está disponible para dispositivos iPhone, iPad y Android.
                                <h3>Acceso por cable</h3>
                                <p align="justify">El access point Cisco Business 145AC permite el acceso por cable a través de un solo puerto Gigabit Ethernet. Admite modos de operación completos con alimentación PoE 802.3af. El 145AC viene con tres puertos Gigabit Ethernet locales, un puerto Gigabit Ethernet de enlace ascendente y un puerto RJ-45 de paso pasivo, lo que permite una variedad de conexiones.
                                <h3>Montaje</h3>
                                <p align="justify">Este access point compacto está diseñado con opciones de montaje flexibles en mente. Puede montarlo directamente en la pared o en numerosos estándares de unión de pared globales. Es fácil de instalar y el hardware de montaje está incluido.
                           
