                                    <td class="smallText"><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">El estándar IEEE 802.11ax, también conocido como High-Efficiency Wireless (HEW) o Wi-Fi 6, se basa en 802.11ac. Ofrece una mejor experiencia en entornos típicos con un rendimiento más predecible para aplicaciones avanzadas como video 4K u 8K; aplicaciones de colaboración de alta densidad y alta definición; oficinas completamente inalámbricas; y el Internet de las Cosas (IoT). Wi-Fi 6E es Wi-Fi 6 "extendido" en la banda de frecuencia de 6 GHz.</font></font></td>
