									Modo de tres radios: una radio 4x4 de 6 GHz, una radio 8x8 de 5 GHz y una radio 4x4 de 2,4 GHz.
									Modo de radio cuádruple: una radio 4x4 de 6 GHz, radios duales 4x4 de 5 GHz y una radio 4x4 de 2,4 GHz. Con cuatro radios 4x4 en total (radio cuádruple) dentro del punto de acceso, la capacidad del dispositivo del cliente se puede aumentar a pedido.
									El modo predeterminado del punto de acceso es tri-radio con 8x8 5 GHz y 4x4 para 6 GHz y 2,4 GHz. Tiene la capacidad de dividir la radio 8x8 en dos radios 4x4 de 5 GHz separadas a través de una futura actualización de software, lo que permite los beneficios de FRA y permite que las radios de 2,4 y 6 GHz permanezcan activas.
