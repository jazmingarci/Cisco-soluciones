                                    <td><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Punto de acceso inteligente</font></font></strong></td>
                                    <td><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Smart AP hace que el punto de acceso cambie su consumo de energía para reflejar su carga actual de clientes. Por lo general, un punto de acceso funcionará con las radios que se le proporcionen, independientemente de cuántos clientes estén conectados. Con Smart AP, si hay un número lo suficientemente pequeño de clientes, el punto de acceso reducirá automáticamente el conteo de transmisión de radio, ahorrando energía. (Disponible con una futura actualización de software).</font></font></td>
                                </tr>
                                
                                <tr>
                                    <td><strong><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">Dirección de banda</font></font></strong></td>
