                                <h3>Los access points de la serie Cisco Catalyst 9136 son la próxima generación de puntos de acceso empresariales que admiten la nueva banda de 6 GHz para Wi-Fi. Son resistentes, seguros e inteligentes.</h3>
                                <p align="justify">La serie Catalyst 9136 compatible con Wi-Fi 6E aprovecha la expansión de la banda de 6 GHz para producir una red más confiable y segura, con mayor rendimiento, más capacidad y menos interferencia de dispositivos. Los puntos de acceso vienen con dos radios 4x4 y una radio 8x8 y brindan una serie de funciones.</p>
                                <p align="justify">La tecnología Wi-Fi 6E extiende Wi-Fi 6 al espectro de 6 GHz, brindando velocidades más rápidas y una latencia más baja, al mismo tiempo que proporciona más seguridad a la red. A medida que las aplicaciones se vuelven cada vez más vitales para realizar el trabajo, los clientes necesitan acceso sin trabas a ellas. Wi-Fi 6E proporciona ese acceso directo.</p>
                                
                                
                               <h3>Características clave:</h3>
                              <ul>
                                    <li>La velocidad de la red aumenta debido al nuevo espectro de 6 GHz y al ancho de banda adicional.</li>
                                    <li>Un nuevo estándar de confiabilidad y previsibilidad de conexión que está acortando la brecha entre las conexiones inalámbricas y por cable. Los dispositivos Wi-Fi 6E no tendrán que compartir el mismo espectro con ningún dispositivo que no sea compatible con Wi-Fi 6E y tendrán 1,2 GHz adicionales de espectro sin interferencias para ellos solos.</li>
									<li>Mayor seguridad de red que nunca, gracias a la seguridad WPA3 obligatoria.</li>
                              </ul>

