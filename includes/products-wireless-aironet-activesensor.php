                                <h3>Descripción del producto</h3>
                                <h4>El sensor activo Cisco Aironet es un sensor de red inalámbrico dedicado diseñado para garantizar un rendimiento óptimo en toda la red.</h4>
                                <p align="justify">Aironet Active Sensor es un sensor de red compacto diseñado para monitorear su red inalámbrica.
                                    
                                <p align="justify">Con Aironet Active Sensor, su departamento de TI puede simular experiencias de clientes del mundo real para que pueda validar el rendimiento inalámbrico para lugares críticos y ubicaciones de alto valor, como salas de conferencias y salas de reuniones. Además, una prueba programada por el sensor ayuda a garantizar que su red pueda manejar una afluencia de clientes VIP para que puedan tener una experiencia inalámbrica fluida sin caídas ni retrasos. ¿Cómo hace todo esto este dispositivo? Gracias a Cisco AirProbe, Aironet Active Sensor puede probar de manera proactiva y predecir con precisión la experiencia del usuario.
                                    
                                <p align="justify">Esto significa que su red inalámbrica no se quedará corta cuando una mayor cantidad de dispositivos se conecten a la red. Ya sabrá cuánto ancho de banda está disponible inmediatamente antes del evento y puede ajustarlo en consecuencia.
                                    
                                <p align="justify">Active Sensor no solo simula y predice brillantemente el rendimiento inalámbrico, sino que también es el dispositivo perfecto para asegurarse de que su red esté funcionando a su máximo potencial. Usando Active Sensor, puede probar su red con capacidades de monitoreo proactivo. Esta capacidad le permite obtener información sobre posibles problemas antes de que sea demasiado tarde.
                                <p align="justify">Solo Cisco puede proporcionar información de alta fidelidad a nivel del suelo.
                                <p align="justify">El sensor activo se puede enchufar en cualquier lugar. Otros productos de sensores a menudo están al nivel del techo, lo que a veces proporciona a TI una evaluación de la red menos que precisa, ya que la mayoría de los clientes están a la altura de los ojos. Cuando se instala en el nivel en el que se encuentran la mayoría de los dispositivos móviles, el sensor activo permite una mejor comprensión y una simulación más completa de los clientes reales.
                                <p align="justify">El sensor activo Aironet cumple totalmente con el estándar 802.11ac Wave 2 y tiene soporte completo de Cisco DNA Assurance con Cisco DNA Center. Viene con múltiples opciones de alimentación:
                                  <ul>
                                    <li>Enchufe de alimentación de CA directo</li>
                                    <li>Alimentación a través de Ethernet (PoE)</li>
                                    <li>Alimentación micro USB</li>
                                  </ul>
                                <p align="justify">Aironet Active Sensor facilita la resolución de problemas y mejora el contexto que se muestra en Cisco DNA Assurance. Cisco DNA Assurance pone sus datos a trabajar, obtiene información contextual de 360 ​​grados sobre usuarios, dispositivos y aplicaciones, y asegura el rendimiento de la red con análisis de datos históricos y en tiempo real, para aprender, adaptarse e incluso detectar problemas antes de que sucedan.

                              
