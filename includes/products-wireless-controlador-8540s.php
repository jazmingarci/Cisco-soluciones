                                <h2>Visión de conjunto:</h2>
                                <p align="justify">El controlador inalámbrico Cisco 8540 proporciona control, administración y solución de problemas centralizados para implementaciones a gran escala en implementaciones de proveedores de servicios y grandes campus. Ofrece flexibilidad para admitir múltiples modos de implementación en el mismo controlador: por ejemplo, modo centralizado para campus, modo Cisco FlexConnect™ para sucursales eficientes administradas a través de la WAN y modo de malla (puente) para implementaciones en las que no se dispone de cableado Ethernet completo. Como componente de la red inalámbrica unificada de Cisco, este controlador proporciona comunicaciones en tiempo real entre los puntos de acceso Cisco Aironet®, la infraestructura Cisco Prime® y el motor de servicios de movilidad de Cisco, y es interoperable con otros controladores de Cisco.</p><br>
                                <p align="justify">La arquitectura de red digital de Cisco (Cisco DNA) es una arquitectura abierta y extensible impulsada por software que acelera y simplifica las operaciones de red de su empresa. La arquitectura programable libera a su personal de TI de tareas de configuración de red repetitivas y que consumen mucho tiempo para que puedan concentrarse en la innovación que transforma positivamente su negocio. SD-Access, como parte de Cisco DNA, permite la automatización basada en políticas desde el borde hasta la nube con capacidades fundamentales. Cisco DNA Assurance, también parte de Cisco DNA, proporciona una fuente única para monitorear, modificar y administrar su red y datos de aplicaciones.</p><br>
                                <p align="justify">El controlador inalámbrico Cisco 8540, optimizado para el rendimiento 802.11ac Wave2, alta escala y tiempo de actividad del sistema mejorado, admite:</p><br>
                                 <ul class="listas">
                                     <li>Telemetría de transmisión y programabilidad impulsada por la intención.
                                     <li>Punto de acceso de subsegundos y conmutación por error del cliente para una disponibilidad ininterrumpida de la aplicación.</li>
                                     <li>Visibilidad extraordinaria del tráfico de aplicaciones, utilizando Cisco Application Visibility and Control (AVC), la tecnología que incluye el motor Network-Based Application Recognition 2 (NBAR2), la capacidad de inspección profunda de paquetes (DPI) de Cisco. Esto permite marcar, priorizar y bloquear para conservar el ancho de banda de la red y mejorar la seguridad. Los clientes pueden exportar opcionalmente los flujos a Cisco Prime Infrastructure o a un recopilador NetFlow de terceros.</li>
                                     <li>Motor integrado de clasificación de políticas inalámbricas Bring-Your-Own-Device (BYOD) que permite la clasificación de dispositivos cliente y la aplicación de políticas basadas en grupos de usuarios.</li>
                                     <li>Implementación de acceso de invitados y servicios Bonjour y Chromecast en implementaciones centralizadas.</li>
                                     <li>Segmentación definida por software con tecnología Cisco TrustSec®, que reduce el mantenimiento, la complejidad y los gastos generales de la lista de control de acceso (ACL).</li>
                                     <li>Tecnología Cisco CleanAir® integrada, que proporciona la única red inalámbrica de autorreparación y optimización automática del sector.</li>
                                     <li>Wi-Fi Alliance Passpoint (Hotspot 2.0) para descarga de datos móviles.</li>
                                     <li>Gestión de movilidad basada en red con Proxy Mobile IPv6 Mobility Access Gateway (MAG) y compatibilidad con eGRE para la integración con redes de datos móviles.</li>
                                     <li>Asistente de GUI simplificado para una configuración rápida y paneles intuitivos para monitorear y solucionar problemas.</li>
                                     <li>Cisco DNA y SD-Access Wireless, así como Cisco DNA Assurance.</li>
                                </ul>
