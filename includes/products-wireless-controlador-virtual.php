                                <h2>Visión de conjunto:</h2>
                                <p align="justify">El controlador de LAN inalámbrica virtual de Cisco (vWLC) está disponible con dos tipos de imágenes de software: imagen a pequeña escala (admite hasta 200 puntos de acceso y 6000 clientes) e imagen a gran escala (admite hasta 3000 puntos de acceso y 32000 clientes). El controlador virtual admite el acceso seguro de invitados, la detección de rogue para el cumplimiento de la industria de tarjetas de pago (PCI) y voz y video Wi-Fi en la sucursal (conmutado localmente).</p><br>
                                <p align="justify">El controlador inalámbrico virtual de Cisco proporciona control, administración y solución de problemas centralizados. Es compatible con la solución Cisco FlexConnect™. El tráfico de datos de los puntos de acceso se conmuta localmente en el punto de acceso.</p><br>
                                <p align="justify">El controlador inalámbrico virtual de Cisco automatiza las funciones de administración y configuración inalámbrica y permite a los administradores de red tener la visibilidad y el control necesarios para administrar, proteger y optimizar de manera rentable el rendimiento de sus redes inalámbricas. El controlador inalámbrico virtual de Cisco en modo conmutado central es compatible con el directorio de servicios de Bonjour para permitir que los servicios de Bonjour se anuncien y utilicen en una red L3 separada. El motor de política inalámbrica es una característica de política y perfilador inalámbrico en el controlador inalámbrico que permite la creación de perfiles de dispositivos inalámbricos y la aplicación de políticas tales como asignación de VLAN, calidad de servicio (QoS), ACL y acceso basado en la hora del día. Como componente de la arquitectura de red digital (DNA) de Cisco, este controlador proporciona comunicaciones en tiempo real entre los puntos de acceso Cisco Aironet®, la infraestructura principal de Cisco y el motor de servicios de movilidad de Cisco, y es interoperable con otros controladores de Cisco. Con la tecnología Cisco CleanAir® integrada, el controlador inalámbrico virtual de Cisco proporciona la única red inalámbrica de autorreparación y optimización automática del sector para pequeñas y medianas empresas, así como para grandes empresas.</p><br>
                                
