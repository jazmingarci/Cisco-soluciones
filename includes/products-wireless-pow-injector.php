                                <h2>Inyector de corriente Cisco Aironet</h2>
                                <p align="justify">Los productos Cisco® Power Injector aumentan la flexibilidad de implementación de los puentes y puntos de acceso inalámbricos Cisco Aironet al proporcionar una opción de alimentación alternativa a la alimentación local, conmutadores multipuerto con capacidad de alimentación en línea y paneles de conexión de alimentación multipuerto.</p>

                                <p align="justify">El inyector de energía para los puntos de acceso de las series Cisco Aironet 1100 y 1200 (AIR-PWRINJ3) funciona con la fuente de alimentación provista con el punto de acceso.</p>
                                <p align="justify">El convertidor de medios con inyector de energía Cisco Aironet (AIR-PWRINJ-FIB) convierte los medios de fibra en medios de categoría 5 y combina la señal de datos resultante con energía para entregarla al punto de acceso o al puente. El convertidor de medios del inyector de alimentación acepta alimentación de 48 VCC del conector cilíndrico de la fuente de alimentación local o de una fuente de alimentación alternativa de 48 VCC. Cuando se alimenta con una fuente de alimentación alternativa de 48 V CC conectada mediante el cable flexible de suministro de alimentación proporcionado, el convertidor de medios del inyector de energía cuenta con la certificación UL2043 y es adecuado para su instalación en espacios de aire ambiental. La fuente de alimentación local se proporciona con los puntos de acceso de las series Cisco Aironet 1100, 1130AG, 1200, 1230AG y 1240AG.</p>
                                <p align="justify">La cantidad de módulos de radio determina qué conmutador Ethernet de Cisco puede alimentar el punto de acceso de la serie Aironet 1250. Hay dos escenarios:</p>

                                <ul>
                                   <li>Punto de acceso de la serie Aironet 1250 con un módulo de radio RM1252 instalado</li>
                                </ul> 

                                <p align="justify">El punto de acceso de la serie Aironet 1250 con un módulo de radio RM1252 instalado requiere 12,95 W, que se encuentra dentro del estándar 802.3af Power over Ethernet. Se puede usar cualquier conmutador Cisco compatible con 802.3af para alimentar el punto de acceso de la serie Aironet 1250 con un módulo de radio RM1252 instalado.</p>
                                <ul>
                                    <li>Punto de acceso de la serie Aironet 1250 con dos módulos de radio RM1252 instalados</li>
                                </ul>
                                  <p align="justify">A partir de fines de 2007, Cisco habilitará la alimentación de puerto único con negociación automática para el punto de acceso Aironet 1250 en los principales conmutadores de la cartera Catalyst. Esta solución única e integrada proporciona todos los requisitos de alimentación para los módulos de radio dual y elimina la necesidad de ejecutar una caída de cableado adicional o insertar un inyector de alimentación independiente.</p>
                                  <p align="justify">El inyector de energía para los puntos de acceso de la serie Cisco Aironet 1250 (AIR-PWRINJ4) contiene una fuente de alimentación integrada que convierte de 100 a 240 voltios de CA en 56 voltios de CC y luego inyecta esta energía en el cable Ethernet de categoría 5 para alimentar el punto de acceso.</p>
                                        <center><img class="img-responsive" src="images/Wireless/Power-injector-diagram.jpg" alt=""></center>
                                        <p align="justify"> Como se muestra en la Tabla 3, un inyector de energía Cisco Aironet se puede configurar para ordenar o se puede pedir como un artículo separado. Si el inyector de energía está configurado con el pedido del punto de acceso, se enviará dentro de la caja de embalaje del punto de acceso. Si el inyector de potencia se pide como repuesto, se enviará en una caja separada.</p>
